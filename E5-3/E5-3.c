/*****************************************************************************
* @作    者:peteryang0601
* @函数名:dist
* @功能描述:计算坐标点间的距离
* @输入参数:x1:起点X轴坐标,y1:起点Y轴坐标,x2:终点X轴坐标,y1:终点Y轴坐标,
* @输出参数:NONE
* @返 回 值:NONE
* @备    注:
*****************************************************************************/
double dist( double x1, double y1, double x2, double y2 )
{
	double distance = 0.0d;

	distance = sqrt((x2 - x1)*(x2 - x1) + (y2 - y1)*(y2 - y1));
	return distance;
}

