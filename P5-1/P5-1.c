#include <stdio.h>

int sum(int m, int n);

int main()
{    
    int m, n;

    scanf("%d %d", &m, &n);
    printf("sum = %d\n", sum(m, n));

    return 0;
}


/*****************************************************************************
* @作    者:阳永锋
* @函数名:sum
* @功能描述:计算m到n之间所有整数的和
* @输入参数:m:求和区间起始值,n:求和区间结束值
* @输出参数:NONE
* @返 回 值:和
* @备    注:m < n
*****************************************************************************/
int sum(int m, int n)
{
	int temp = m;
	int sum = 0;
	
	while(temp <= n)
	{
		sum += temp;
		temp++;
	}
	return sum;
}

