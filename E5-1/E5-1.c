#include <stdio.h>

int sign( int x );

int main()
{
    int x;

    scanf("%d", &x);
    printf("sign(%d) = %d\n", x, sign(x));

    return 0;
}

/*****************************************************************************
* @作    者:peteryang0601
* @函数名:sign
* @功能描述:符号函数
* @输入参数:NONE
* @输出参数:NONE
* @返 回 值:NONE
* @备    注:若x大于0，sign(x) = 1；若x等于0，sign(x) = 0；否则，sign(x) = -1。
*****************************************************************************/
int sign(int x)
{
	if (x > 0)
	{
		return 1;
	}
	else if (x == 0)
	{
		return 0;
	}
	else
	{
		return -1;
	}
}

